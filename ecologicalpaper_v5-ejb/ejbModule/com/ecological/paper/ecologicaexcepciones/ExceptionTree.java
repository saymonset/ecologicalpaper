package com.ecological.paper.ecologicaexcepciones;

public class ExceptionTree extends Exception{
    
    /** Creates a new instance of EcologicaExcepciones */
    public ExceptionTree() {
        this("EcologicaExcepciones");
    }
    public ExceptionTree(String msg) {
        super(msg);
    }
    
    public ExceptionTree(Throwable t){
        super(t);
    }
    
    public ExceptionTree(String msg, Throwable t){
        super(msg,t);
    }
    
}
