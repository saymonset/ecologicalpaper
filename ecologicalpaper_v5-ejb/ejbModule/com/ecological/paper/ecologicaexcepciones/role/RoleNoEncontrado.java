public class RoleNoEncontrado extends Exception{
    
    /** Creates a new instance of RoleNoEncontrado */
    public RoleNoEncontrado() {
        this("EcologicaRoleExcepciones");
    }
    public RoleNoEncontrado(String msg) {
        super(msg);
    }
    
    public RoleNoEncontrado(Throwable t){
        super(t);
    }
    
    public RoleNoEncontrado(String msg, Throwable t){
        super(msg,t);
    }
}
