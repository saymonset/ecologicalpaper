function mostrarCorreoAlternativo(id1, id2){
	
    var camp1 = document.getElementById(id1);    
    var camp2 = document.getElementById(id2);
    var c1 = document.getElementById("operacionesCriticas:correoTempSelect:1").checked;
    
    if(c1){
    	camp1.className = '';
    	camp2.className = '';
    }else{
    	camp1.className = 'ocultar';
    	camp2.className = 'ocultar';
    }

}